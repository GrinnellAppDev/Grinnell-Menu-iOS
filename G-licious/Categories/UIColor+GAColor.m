#import "UIColor+GAColor.h"

@implementation UIColor (GAColor)

+ (UIColor *) scarletColor
{
    return [UIColor colorWithRed:135/255.f green:1/255.f blue:6/255.f alpha:1.0];
}

+ (UIColor *)lightScarletColor
{
    return [UIColor colorWithRed:240/255.f green:146/255.f blue:146/255.f alpha:1.0];
}

@end
